
public class Vizinho {

    String nome;
    Float peso;

    public String getNome() {
        return nome;
    }

    public Float getPeso() {
        return peso;
    }

    public void setPeso(Float peso) {
        this.peso = peso;
    }

    public Vizinho(String nome, Float peso) {
        this.nome = nome;
        this.peso = peso;
    }
}
